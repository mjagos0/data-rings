func (b *localBackend) PublicState(n *TestNode) (NodeState, error) {
	resp, err := http.Get(b.apiURL(n) + "/debug/state")
	if err != nil {
		return NodeState{}, fmt.Errorf("node %s: %w", n.Name_, err)
	}
	defer resp.Body.Close()
	var s NodeState
	if err := json.NewDecoder(resp.Body).Decode(&s); err != nil {
		return NodeState{}, fmt.Errorf("node %s: decode: %w", n.Name_, err)
	}
	return s, nil
}

func (b *localBackend) PrivateState(n *TestNode, groupName string) (PrivateRingEntry, bool) {
	resp, err := http.Get(b.apiURL(n) + "/debug/groups")
	if err != nil {
		return PrivateRingEntry{}, false
	}
	defer resp.Body.Close()
	var entries []PrivateRingEntry
	if err := json.NewDecoder(resp.Body).Decode(&entries); err != nil {
		return PrivateRingEntry{}, false
	}
	for _, e := range entries {
		if e.Name == groupName || strings.HasPrefix(e.GroupID, groupName) {
			return e, true
		}
	}
	return PrivateRingEntry{}, false
}

func (b *localBackend) Exec(n *TestNode, args ...string) (string, error) {
	b.mu.Lock()
	lp := b.procs[n.Name_]
	b.mu.Unlock()
	apiAddr := fmt.Sprintf("http://127.0.0.1:%d", lp.apiPort)
	cmdArgs := append([]string{"-api", apiAddr}, args...)
	cmd := exec.Command(cliBinPath, cmdArgs...)
	out, err := cmd.CombinedOutput()
	return strings.TrimSpace(string(out)), err
}

func (b *localBackend) Stop(n *TestNode) error {
	b.mu.Lock()
	lp := b.procs[n.Name_]
	b.mu.Unlock()
	if lp == nil || lp.cancel == nil {
		return nil
	}
	lp.cancel()
	lp.cmd.Wait()
	lp.cancel = nil
	lp.cmd = nil
	return nil
}

func (b *localBackend) Start(n *TestNode) error {
	b.mu.Lock()
	lp := b.procs[n.Name_]
	b.mu.Unlock()
	if lp == nil {
		return fmt.Errorf("node %s has no local proc entry", n.Name_)
	}
	b.startDaemon(b.t, n.Name_, lp)
	return nil
}

func (b *localBackend) StoreFile(n *TestNode, data []byte, group string) (string, error) {
	tmpDir, err := os.MkdirTemp("", "drings-store-*")
	if err != nil {
		return "", err
	}
	defer os.RemoveAll(tmpDir)
	fp := filepath.Join(tmpDir, "testdata.bin")
	if err := os.WriteFile(fp, data, 0644); err != nil {
		return "", err
	}
	out, err := b.Exec(n, "add", fp)
	if err != nil {
		return "", fmt.Errorf("add: %w\n%s", err, out)
	}
	cidStr := ParseCID(out)
	if cidStr == "" {
		return "", fmt.Errorf("could not parse CID from add output: %s", out)
	}
	if group != "" {
		out, err = b.Exec(n, "pub", group, cidStr)
		if err != nil {
			return cidStr, fmt.Errorf("pub (private): %w\n%s", err, out)
		}
	} else {
		out, err = b.Exec(n, "pub", cidStr)
		if err != nil {
			return cidStr, fmt.Errorf("pub (public): %w\n%s", err, out)
		}
	}
	return cidStr, nil
}

