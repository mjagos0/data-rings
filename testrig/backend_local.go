func (b *localBackend) RestartOnNewPort(n *TestNode, newDHTPort, newAPIPort int) error {
	if err := b.Stop(n); err != nil {
		return fmt.Errorf("stop %s: %w", n.Name_, err)
	}
	b.mu.Lock()
	lp := b.procs[n.Name_]
	if lp == nil {
		b.mu.Unlock()
		return fmt.Errorf("%s: no proc entry", n.Name_)
	}
	lp.dhtPort = newDHTPort
	lp.apiPort = newAPIPort
	b.mu.Unlock()
	b.startDaemon(b.t, n.Name_, lp)
	return nil
}

func (b *localBackend) RingStorage(n *TestNode, ringID string) (int64, int64, error) {
	resp, err := http.Get(b.apiURL(n) + "/debug/rings/" + ringID + "/storage")
	if err != nil {
		return 0, 0, fmt.Errorf("node %s: %w", n.Name_, err)
	}
	defer resp.Body.Close()
	var result struct {
		Used	int64	`json:"used_bytes"`
		Max	int64	`json:"max_bytes"`
	}
	if err := json.NewDecoder(resp.Body).Decode(&result); err != nil {
		return 0, 0, fmt.Errorf("node %s: decode: %w", n.Name_, err)
	}
	return result.Used, result.Max, nil
}

func (b *localBackend) Close() error {
	b.mu.Lock()
	defer b.mu.Unlock()
	for _, lp := range b.procs {
		if lp.cancel != nil {
			lp.cancel()
			lp.cmd.Wait()
		}
		if lp.logFile != nil {
			lp.logFile.Close()
		}
	}
	return nil
}

func (b *localBackend) NodeLogPath(name string) string {
	b.mu.Lock()
	lp := b.procs[name]
	b.mu.Unlock()
	if lp == nil {
		return ""
	}
	return lp.logPath
}

func (b *localBackend) NodeLogs(name string) (string, error) {
	path := b.NodeLogPath(name)
	if path == "" {
		return "", fmt.Errorf("no log file for %s", name)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		return "", err
	}
	return string(data), nil
}

func (b *localBackend) NodeLogsTail(name string, n int) (string, error) {
	content, err := b.NodeLogs(name)
	if err != nil {
		return "", err
	}
	lines := strings.Split(content, "\n")
	if len(lines) > n {
		lines = lines[len(lines)-n:]
	}
	return strings.Join(lines, "\n"), nil
}

func (b *localBackend) NodeLogsSince(name string, since, until time.Time) (string, error) {
	return b.NodeLogs(name)
}
